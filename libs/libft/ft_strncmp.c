#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)

{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	else if (n == 1)
		return ((unsigned char)*s1 - (unsigned char)*s2);
	while (i < n && s1[i] == s2[i] && s1[i])
		i++;
	if (i == n)
		return (0);
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
